                        壹、 赛事理念
                    联合国教科文组织表明，独立思考是未来年轻人必须具备的重要竞争力。亚洲思辨教育学会坚持「以中文为基础，从母语出发」，培养学生深度思考与议论写作的能力，并借鉴北欧的思辨教育教学方式，提高知识的互动性，让学科的边界感降低。
                    透过书写，不仅促成语文的学习，更能带来思维的碰撞。


                    思辨，不是只有口说的形式；
                    辩论，更不止于语言的输出。

                    「亚洲思辨写作对抗赛」希望参赛者透过敲击键盘，强化个人观点的论证与论述，透过书写，展现深度思考的力量——让文字从单向变成双向，从输出变成对抗。
                    亚洲思辨教育学会筹备一年半，将写作与辩论结合，并打破地域的限制。让全球热爱中文的青少年可以同场以文字进行即时的思想竞技，让思辨的盛宴在亚洲杯中文辩论赛之外持续扩张，让我们一起——「用中文思辨，用写作看世界」。

                        貳、 比赛特色
                        1. 全亚洲首创写作思辨赛事：首个以书写文字作为比赛形式的思辨之旅，独家研发线上即席反驳系统，突破传统形式，升级思辨体验，让写作不再枯燥。
                        2. 国际性规模让各地选手汇聚一堂：利用线上平台跨越距离与时间的限制，让来自大陆、港澳台、美加、澳洲、纽西兰、星马泰等地同年龄段的思辨精英互动式临场写作，鼓励多元独立思考的交流。
                        3. 大咖语文名师与思辨专家强强联手：评审团由超过30所国际学校中文校长、主任受邀，与30位顶级思辨教育工作者汇集而成，确保批卷素质专业；凡报名参加比赛之选手均可参加由评审亲自主持的思辨讲座。

