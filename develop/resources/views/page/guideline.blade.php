                        肆、 辩题范畴
                    亚洲思辨教育学会所主办的赛事均把讨论议题聚焦在联合国所公告的17项可持续发展目标，结合教育时事，延伸至未来趋势与文化传承。
                    第一届亚洲思辨写作对抗赛的主题发展亦然，每道题目皆来自以下五大领域范畴，希望参赛者累积平日对这五个领域的知识获取，进而建构自己的观点，以文字论证的方式完成赛事。
                    为考验学生在赛场上写作的即时组织与论证能力，主办方赛前仅提供比赛范畴，赛事辩题皆会出自比赛范畴内，唯其深浅程度因年龄组别而异，完整辩题将在比赛正式开始时同步公开。


                        1. 动物权益：
                    联合国经济与社会理事会指出，可持续发展的三大支柱为经济发展、社会公平和环境保护，三大支柱必须同时兼顾，统一协调——动物保护正正与三个支柱紧密相连。
                    Animal，来自于拉丁词根Anim，代表「生命」。人类在指名识物之初，就有万物有灵的体悟，动物福利的概念就是源自于此。孟子认为：「人皆有不忍人之心」同理心是家门兴旺、四海和睦的根基。从饲养照护、动物经济，到动物福利与动物平权——由浅入深的题目，在人与动物的篇章中，是我们必须重点关注的主题。


                        2. 公平正义：
                    在可持续发展的目标当中，有三分之一都与公平正义的主题有关。社会上的饥饿与贫穷、教育中的资源平等、性别上的平等尊重，到国际间的平等合作——公平，一直是最核心基础的价值。
                    不论是个体自身、群体社会，甚至国际世界，每一个环节都须由公平正义来贯穿。这个课题衍生关注的议题很多，主办方特此选取精华出题。


                        3. 历史文化：
                    价值观的建立，从孕育自己的土地开始做起。传统中华文化博大精深，与现代化变革的冲突与融合无所不在。
                    我们认识过去，以古鉴今，华人作为物质文化遗产与非物质文化遗产加总最多的民族，应当优先认识自己，进而认识世界。对历史文化的思辨，是我们的理念与目标。


                        4. 环境保护：
                    可持续发展目标中，能量资源是人类追求永续的重中之重。食物的可持续追求、能源的可持续发展、水资源与海洋资源的共同维护、环境保护的推动、可持续性的经济发展......世界公民的责任在「人与环境」的篇章中处处可见，各国与环境共处，与资源和解的方案推陈出新。思辨，又称素养教育，而素养本身，就在于我们对于生活环境的态度与观念。因此，首届的赛事我们也以环境保护列为出题方向之一。


                        5. 科技发展：
                    未来，是网络的时代，是科技的时代，是大数据的时代。
                    「人与科技」的篇章探讨的是科学技术的未来发展方向与边界。在大量信息快速交替的网络世代，科技公民该如何沉着冷静阅读、消化信息，并且有逻辑的分析与整理，更突显了独立思考的重要。
                    科技的变再快，都值得人类停下脚步，用文字笔杆在每个议题反思，我们才真正的能引领科技，掌控生活。

                        伍、 规则介绍
                        1. 选手独立参赛，每场赛事共分成「立论」与「反驳」两轮，每轮作答时限及字数分别为：
                        • A组（19岁以下组别）：
                        i. 「立论」环节须于80分钟内 最少撰写600字；
                        ii. 「反驳」环节须于40分钟内 最少撰写300字。
                        • B组（16岁以下组别）：
                        i. 「立论」环节须于70分钟内 最少撰写450字；
                        ii. 「反驳」环节须于30分钟内 最少撰写200字。
                        • C组（13岁以下组别）：
                        i. 「立论」环节须于60分钟内 最少撰写300字；
                        ii. 「反驳」环节须于30分钟内 最少撰写120字。

                    注：每轮限时结束时，选手所输入之内容将被系统自动提交，不达最低字数限制之文稿将遭扣分。
                        2. 选手必须打开摄像头才能开始比赛，比赛过程中亦须全程保持摄像头开启，清晰显示由本人作答。
                        3. 每轮作答头30分钟选手不得离开座位；第一轮「立论」环节不设提早交卷，但选手可于第二轮「反驳」环节提早交卷。
                        4. 比赛进行期间，选手可运用语音输入以外的任何一种中文输入法打字；繁、简体由选手自由选择。
                        5. 选手可自由上网查阅资料，但所有作答须由本人独自输入完成，不可有他人在。


                    旁指导或代替作答；若发现选手稿件由他人代写，或于比赛过程中经由他人帮忙完成，将被直接取消资格，以维持赛事公平性。
                        6. 建议选手最低网速1MB，所有连线或技术问题须在测试环节提出，并于正式比赛前解决，赛期若出现连线或其他技术问题须由选手自行承担后果。
                        7. 主办方将按照学术抄袭惯例系统查核所有晋级或得奖作品，若文稿涉及整段抄袭，该场评审将共同商议并重新评核。
                        8. 主办方于评核过程将保留每份作品的匿名性，评审会按照作品编号给予评分，选手不得于文稿中直接提及自己的姓名或间接暗示身分。

                        陸、 赛果奖项
                        1. 每轮比赛结束后，评审会按照「立论」和「驳论」两个环节给予分数和评语。
                        2. 初赛阶段每份作品将获一位中文名师和一位思辨导师批阅，决赛阶段每份作品将获三位中文名师和三位思辨导师批阅。
                        3. 评审批卷将按照比赛配对同时阅读正、反方所撰写的文稿，并为双方的立论稿与反驳稿分别评分，最终晋级赛果以每组的总分作排名计算。
                        4. 每轮评分项目如下：
                        i. 立论稿件（200分）
                    根据选手所抽到的立场站方评核开篇论点的论证完整度，具体评分包括三大面向：扣题的紧密程度、论点的切入视角，及论述的细致程度。


                        ii. 反驳稿件（200分）
                    根据选手对应对手的立论所撰写的反驳稿件给予评分，具体评分包括三大面向：回应的精准程度、反驳的论证强度，及反驳的说服技巧。
                        iii. 推论证明（50分）
                    综合选手在立论稿及反驳稿的表现，审核推论与证明的过程中，论证技巧使用是否得宜、举例类比是否恰当、资料运用及诠释是否合乎逻辑和具说服力。
                        iv. 文字用词（50分）
                    综合选手在立论稿及反驳稿的表现，审核整体用字的精准能力，包括标题是否明确清晰、词句是否通顺、词汇量是否丰富，及错别字的多寡。


                        5. 所有选手均可获《亚洲思辨写作对抗赛参赛证明书》中、英文各一版，及神秘纪念品礼包一份。
                        6. 凡入围决赛选手均可获「入选奖」及《亚洲思辨写作对抗赛作品精选集》，并按照排名获颁「一等奖」、「二等奖」、「三等奖」；其文稿将会于线上平台及作品选集发布。
                        7. 每组决赛选手将竞逐「最佳立论奖」和「最佳反驳奖」；「一等奖」人选中排名前9位可获颁：
                            i. 「冠军」奖座及奖牌（1位）、
                            ii. 「亚军」奖座及奖牌（3位）、
                            iii. 「季军」奖座及奖牌（5位）。


                        8. 比赛另设奖项予老师，选手如有指导老师，可于报名时可填写老师姓名，予获「一等奖」之选手的指导老师可获得「优秀指导老师奖」。

